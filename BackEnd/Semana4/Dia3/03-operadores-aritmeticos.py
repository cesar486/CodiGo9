# Operadores Aritmeticos
# + suma
# - resta
# * multiplicacion
# / division
# % modulo
# ** exponente
# // cociente
num1 = 10
num2 = 20
num3 = num1+num2
print("La suma es: {}".format(num3))
num3 = num1 - num2
print("La resta es: {}".format(num3))
num3 = num1 * num2
print("La multiplicacion es: {}".format(num3))
num3 = num1 / num2
print("La division es: {}".format(num3))
num3 = num1 % num2
print("El modulo es: {}".format(num3))
num3 = num1 ** num2
print("El resultado de {} elevado a {} es {}".format(num1,num2,num3))
num3 = num1 // num2
print("El cociente de la division entre {} y {} es {}".format(num1,num2,num3))
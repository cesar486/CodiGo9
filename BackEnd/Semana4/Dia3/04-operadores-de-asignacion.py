# Operadores de asignacion
# = Igual
# += Incremento =>      a=a+1  a+=1
# -= Decremento =>      a=a-2  a-=2
# *= Multiplicacion=>   a=a*10 a*=10
# /= Division   =>      a=a/20 a/=20
# **= Potencia  =>      a=a**3 a**=3
num1, num2= 20,5
# es lo mismo que haber puesto num1 = num1 + num2
num1 += num2
print(num1)
# es lo mismo que haber puesto num1 = num1 - num2
num1 -= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 * num2
num1 *= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 / num2
num1 /= num2
print(num1)
# es lo mismo que haber puesto num1 = num1 ** num2
num1 **= num2
print(num1)
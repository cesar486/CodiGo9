# Operadores Logicos
# and = si las dos condiciones son Verdaderas       todo es verdadero
# or = si alguna de las dos son Verdaderas      todo es verdadero
# not = si es Falso es Verdadero y si es Verdadero es falso
# En javascript and = &&, or = ||
print((10<10) and (10<20))
print((10<8) or (10<20))
print(not (10<20))